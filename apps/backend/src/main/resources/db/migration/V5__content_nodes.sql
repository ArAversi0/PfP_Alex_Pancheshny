CREATE TABLE content_nodes (
    id BIGSERIAL PRIMARY KEY,
    section VARCHAR(20) NOT NULL CHECK (section IN ('LORE', 'RULES')),
    slug VARCHAR(180) NOT NULL,
    parent_slug VARCHAR(180),
    title VARCHAR(200) NOT NULL,
    summary TEXT NOT NULL DEFAULT '',
    content_markdown TEXT NOT NULL DEFAULT '',
    sort_order INTEGER NOT NULL DEFAULT 0,
    published BOOLEAN NOT NULL DEFAULT TRUE,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_content_nodes_section_slug UNIQUE (section, slug)
);

CREATE INDEX idx_content_nodes_section_parent ON content_nodes(section, parent_slug, sort_order);
CREATE INDEX idx_content_nodes_section_published ON content_nodes(section, published);

